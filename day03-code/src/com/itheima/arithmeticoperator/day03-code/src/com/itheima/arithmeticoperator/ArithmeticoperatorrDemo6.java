package com.itheima.arithmeticoperator.day03;

public class ArithmeticoperatorrDemo6 {
    public static void main(String[] args) {
        //+=
        int a = 10;
        int b =20;
        a += b;//等同于a = a + b；
        System.out.println(a);
        System.out.println(b);

        short s = 1;
        s += 1;
        System.out.println(s);
    }
}
